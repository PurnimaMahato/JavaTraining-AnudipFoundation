package com.test;

public class ArithmaticOperations {
	//creating method to perform addition operation
	public int add(int m , int n) {
		return m+n;
	}
	//creating method to perform subtraction operation
	public int sub(int m , int n) {
		return m-n;
	}
	//creating method to perform multiplication operation
	public int mul(int m , int n) {
		return m*n;
	}
	//creating method to perform division operation
	public int div(int m , int n) {
		return m/n;
	}

}
